import { TIMELINE_TOOL_ICONS, type TimelineToolIcon } from '../toolIcons';

export type TimelineToolPriority = 'P0' | 'P1' | 'P2';
export type TimelineToolAvailability = 'enabled' | 'future' | 'requires-source';

export interface TimelineToolDefinition {
  id: TimelineToolId;
  groupId: TimelineToolGroupId;
  kind: TimelineToolKind;
  label: string;
  description: string;
  icon: TimelineToolIcon;
  priority: TimelineToolPriority;
  availability: TimelineToolAvailability;
  mutatesTimeline: boolean;
  shortcutActionId?: ShortcutActionId;
  disabledReason?: string;
}

function tool(definition: Omit<TimelineToolDefinition, 'icon'>): TimelineToolDefinition {
  return {
    ...definition,
    icon: TIMELINE_TOOL_ICONS[definition.id],
  };
}

